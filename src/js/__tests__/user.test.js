import sort from '../user';

test('sort heroes', () => {
  const template = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const heroes = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];

  const sortHero = sort(heroes);

  expect(sortHero).toEqual(template);
});

